// Função que recebe dois números como parâmetros e retorna a soma
function somar(n1, n2) {
    return n1 + n2;
}

// Teste da função somar
var numero1 = 10;
var numero2 = 20;
var resultado = somar(numero1, numero2);

// Exibe o resultado no console
console.log("A soma de " + numero1 + " e " + numero2 + " é: " + resultado);
